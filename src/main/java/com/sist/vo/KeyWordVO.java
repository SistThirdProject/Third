package com.sist.vo;

public class KeyWordVO {
	private long time;
	private int rank;
	private String keyword;
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyWord) {
		this.keyword = keyWord;
	}
}
